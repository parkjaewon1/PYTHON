print(a[3])
print(a[10]) # 인덱스 10번째 문자
print(a[-3]) # 끝에서 3번째 문자
print(a[0:4]) # 인덱스 0부서 4바로 앞까지
print(a[5:7]) # 인덱스 5부터 7앞까지
print(a[:10]) # 인덱스 10 앞까지
print(a[10:]) # 인덱스 10부터 끝까지
print(a[:])  # 전체
print(a[10:-2])