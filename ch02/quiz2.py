str1 = "오늘은 2016년 12월 25일 일요일입니다."
print(str1)
print("오늘은 "+str(2016)+"년 "+str(12)+"월 "+str(25)+"일 일요일입니다.")
print("오늘은",2016,"년",12,"월",25,"일 일요일입니다.")
# {} 에 숫자가 없으면 순서대로 대입
print("오늘은 {}년 {}월 {}일 {}입니다.".format(2016, 12, 25,"일요일"))
# {}에 숫자가 있으면 해당하는 인덱스 글자가 들어 간다
print("오늘은 {3}년 {1}월 {2}일 {1}입니다.".format(2016, 12, 25,"일요일"))
print("오늘은 %d년 %d월 %d일 %s입니다." %(2016, 12, 25, "일요일"))