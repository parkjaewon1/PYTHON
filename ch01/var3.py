# \는 문장이 길 때 한줄을 여러줄로 표시하기 위한 것
print("대박 \
    사건")
# 괄호(())로 묶인 경우는 여러 줄이 있어도 같은 줄에 속하는 것으로 인식
print("대박",
      "사건")
